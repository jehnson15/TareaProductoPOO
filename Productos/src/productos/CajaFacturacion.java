    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // Instanciar el cuadro ed dialogo y mostrarlo en pantalla
        ProductoDialog ventana = new ProductoDialog(this, true);
        ventana.setVisible(true);
        //Obtener los valores del dialogo
        if (ventana.getRootPane() == null) {
            return;
        }
        String producto = ventana.getProducto();
        int cantidad = ventana.getCantidad();
        double precio = ventana.getPrecio();
        double total = cantidad * precio;
        //agregar la fila
        Vector fila = new Vector();
        fila.addElement(cantidad);
        fila.addElement(producto);
        fila.addElement(precio);
        fila.addElement(total);
        modelo.addRow(fila);
        calcularTotales();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tablaProductos.getSelectedRow();
    if (filaSeleccionada >= 0) {
        
        modelo.removeRow(filaSeleccionada);
        calcularTotales(); 
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione una fila para eliminar");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         int filaSeleccionada = tablaProductos.getSelectedRow();
    if (filaSeleccionada >= 0) {
       
        String productoActual = modelo.getValueAt(filaSeleccionada, 1).toString();
        int cantidadActual = Integer.parseInt(modelo.getValueAt(filaSeleccionada, 0).toString());
        double precioActual = Double.parseDouble(modelo.getValueAt(filaSeleccionada, 2).toString());
        
        ProductoDialog ventana = new ProductoDialog(this, true);
        
        ventana.setProducto(productoActual);
        ventana.setCantidad(Integer.toString(cantidadActual));
        ventana.setPrecio(Double.toString(precioActual));
              
        ventana.setVisible(true);
        
        
        if (ventana.getRootPane() == null) {
            return;
        }
        
         String nuevoProducto = ventana.getProducto();
        int nuevaCantidad = ventana.getCantidad();
        double nuevoPrecio = ventana.getPrecio();
        double nuevoTotal = nuevaCantidad * nuevoPrecio;
        
        
        modelo.setValueAt(nuevaCantidad, filaSeleccionada, 0);
        modelo.setValueAt(nuevoProducto, filaSeleccionada, 1);
        modelo.setValueAt(nuevoPrecio, filaSeleccionada, 2);
        modelo.setValueAt(nuevoTotal, filaSeleccionada, 3);
        
        
        calcularTotales();
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione una fila para modificar");
    }
    }//GEN-LAST:event_btnModificarActionPerformed

