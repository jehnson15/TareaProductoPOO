    private void calcularTotales() {

        double totalFila = 0.0;
        int totalFilas = modelo.getRowCount();

        for (int i = 0; i < totalFilas; i++) {
            var valorColumnaTotal = modelo.getValueAt(i, 3).toString();
            totalFila += Double.parseDouble(valorColumnaTotal);
        }
        DecimalFormat formatISV = new DecimalFormat("0.##");
        double isv = totalFila * 0.15;
        String isvFormateado = formatISV.format(isv);
        double tp = totalFila + isv;
        txtSubTotal.setText("" + totalFila);
        txtISV.setText(isvFormateado);
        txtTotal.setText("" + tp);

    }

